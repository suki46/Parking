void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	 if (htim->Instance == TIM6){
		 if (HAL_GPIO_ReadPin(GPIOB, global_gpio_pin) == GPIO_PIN_RESET){
			 if (global_gpio_pin == GPIO_PIN_10){
				 for(int i=500; i<=1500; i++){
					 __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, i);
					 DELAY_MS(1);
				 }
				 DELAY_MS(2000);
				 for(int i=1500; i>=500; i--){
					 __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, i);
					 DELAY_MS(1);
				 }
			 }
		 }
		 HAL_TIM_Base_Stop_IT(&htim6);
	 }
	 if (htim->Instance == TIM6){
		 if (HAL_GPIO_ReadPin(GPIOA, global_gpio_pin) == GPIO_PIN_RESET){
			 if (global_gpio_pin == GPIO_PIN_9){
				 for(int i=750; i<=1750; i++){
					 __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, i);
					 DELAY_MS(1);
				 }
				 DELAY_MS(2000);
				 for(int i=1750; i>=750; i--){
					 __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, i);
					 DELAY_MS(1);
				 }
			 }
		 }
		 HAL_TIM_Base_Stop_IT(&htim6);
	 }


}
